#Вводу
#Перший рядок вхідного файлу INPUT.TXT містить два числа: n (1 ≤ n ≤ 1000) – кількість друзів Толіка, і початковий авторитет Толіка. Наступні n рядків містять пари чисел ai та bi. Всі числа є цілими числами за модулем не більше 106.
#Вихід
